export function getLayoutVariant(id) {
  const variants = {
    1: 1,
    2: 2,
    3: 3,
    4: 4,
    5: 3,
    6: 4,
  };
  return variants[id] ?? 1;
}

/**
 * Deterministic layout variant based on slug characters —
 * ensures the same API post always gets the same layout.
 */
function getLayoutFromSlug(slug) {
  if (!slug) return 1;
  const hash = slug
    .split("")
    .reduce((acc, char, idx) => acc + char.charCodeAt(0) * (idx + 1), 0);
  return (hash % 4) + 1;
}

